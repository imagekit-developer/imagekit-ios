import Foundation

public enum FocusType: String {
    case CENTER = "center"
    case TOP = "top"
    case LEFT = "left"
    case BOTTOM = "bottom"
    case RIGHT = "right"
    case TOP_LEFT = "top_left"
    case TOP_RIGHT = "top_right"
    case BOTTOM_LEFT = "bottom_left"
    case BOTTOM_RIGHT = "bottom_right"
    case AUTO = "auto"
}
